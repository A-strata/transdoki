"""Исключения биллингового слоя."""


class BillingError(Exception):
    """Базовый класс для прикладных ошибок биллинга."""


class InsufficientFunds(BillingError):
    """
    На балансе недостаточно средств для операции (pro rata апгрейд,
    подключение модуля и т.п.).

    Текст исключения — человекочитаемый и готов к показу в UI:
    "Требуется 1490 ₽, доступно 500 ₽".
    """


class PlanChangeError(BillingError):
    """
    Некорректная операция смены плана: попытка даунгрейда через upgrade_plan,
    апгрейда через schedule_downgrade, переход на несуществующий/неактивный план.
    """


class ModuleOperationError(BillingError):
    """
    Некорректная операция с модулем: модуль не существует, неактивен,
    уже подключён (с информацией в тексте, не ошибка сама по себе).
    """
